package sook;

public class _10 {
    public static void printSquare(int x) {
        System.out.println(x * x);
    }
    public static void main(String[] args) {
        int value = 2;
        System.out.println(value);
        printSquare(value);
        printSquare(3);
        printSquare(value * 2);
    }
    
//    메임함수에서 value 정수형 변수를 화면에 출력하고 매개변수로 들어간 3개의 함수가 printsquare 함수에서 제곱된다
}
